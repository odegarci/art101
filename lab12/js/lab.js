function sortingHat(str) {
  let len = str.length;
  let mod = len % 4;
  if (mod == 0) {
    return ""
  }
  else if (mod == 1) {
    return ""
  }
  else if (mod == 2) {
    return ""
  }
  else if (mod == 3) {
    return ""
  }
}